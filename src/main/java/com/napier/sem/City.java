package com.napier.sem;

/**
 * Represents a city
 */
public class City {

    /**
     * Represents the city id
     */
    public int city_id;

    /**
     * City's name
     */
    public String city_name;

    /**
     * City's district
     */
    public String district;

    /**
     * the population of the city
     */
    public int population;

}
